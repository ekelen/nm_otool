#include "libft.h"

int		ft_extra_max(int *tab, size_t len)
{
	size_t	i;
	int		max;

	i = -1;
	while (++i < len)
	{
		if (tab[i] > tab[i - 1])
			max = tab[i];
	}
	return (max);
}
