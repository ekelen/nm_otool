#include "libft.h"

int		ft_atoi(const char *str)
{
	int flag;
	int n;

	flag = 1;
	while (*str == '\r' || *str == '\n' || *str == '\f'	\
		|| *str == '\v' || *str == '\t' || *str == ' ')
	{
		str++;
	}
	n = 0;
	if (*str == '-' || *str == '+')
	{
		if (*str++ == '-')
		{
			flag = -flag;
		}
	}
	while (*str >= '0' && *str <= '9')
	{
		n = (n * 10) + (*str++ - '0');
	}
	return (n * flag);
}
