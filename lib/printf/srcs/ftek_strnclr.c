#include "ft_printf.h"

void	ftek_strnclr(char *s, size_t len)
{
	size_t i;

	i = 0;
	if (s)
	{
		i = len - 1;
		while ((int)i >= 0)
		{
			s[i] = '\0';
			i--;
		}
	}
	return ;
}
